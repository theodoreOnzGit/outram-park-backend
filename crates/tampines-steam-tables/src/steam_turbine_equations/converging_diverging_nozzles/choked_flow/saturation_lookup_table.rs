/// Pressure bracket `[p_lo, p_hi]` enclosing the bubble point for `s0`, read
/// from the saturated-liquid (`s_f`) column. `s_f` increases with row index, so
/// this is a plain binary search. Caller guarantees `s0` is within table range.
#[inline]
pub(crate) fn bubble_point_bracket(s0: SpecificHeatCapacity) -> (Pressure, Pressure) {
    let target = s0.get::<joule_per_kilogram_kelvin>();
    let mut lo = 0usize;
    let mut hi = SAT_TABLE.len() - 1;
    while hi - lo > 1 {
        let mid = (lo + hi) / 2;
        if SAT_TABLE[mid][1] < target { lo = mid; } else { hi = mid; }
    }
    (
        Pressure::new::<pascal>(SAT_TABLE[lo][0]),
        Pressure::new::<pascal>(SAT_TABLE[hi][0]),
    )
}

/// Pressure bracket `[p_lo, p_hi]` enclosing the dew point for `s0`, read from
/// the saturated-vapour (`s_g`) column. `s_g` *decreases* with row index, so the
/// comparison is flipped relative to the bubble-point search.
#[inline]
pub(crate) fn dew_point_bracket(s0: SpecificHeatCapacity) -> (Pressure, Pressure) {
    let target = s0.get::<joule_per_kilogram_kelvin>();
    let mut lo = 0usize;
    let mut hi = SAT_TABLE.len() - 1;
    while hi - lo > 1 {
        let mid = (lo + hi) / 2;
        // s_g decreasing: if s_g[mid] > target the root is at higher pressure
        if SAT_TABLE[mid][2] > target { lo = mid; } else { hi = mid; }
    }
    (
        Pressure::new::<pascal>(SAT_TABLE[lo][0]),
        Pressure::new::<pascal>(SAT_TABLE[hi][0]),
    )
}
